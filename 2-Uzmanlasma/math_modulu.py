# round

print(math.sqrt(28))
print(round(math.sqrt(28)))    # round math modulu dışındada çalışan yuvarlama komutu
print(math.sqrt(34))
print(round(math.sqrt(34)))    # yarım altını aşağı yarım üstünü yukarı yuvarlar

print(round(8.1))        # 8
print(round(8.7))        # 9



print("************************************************************")

# ceil 

print(math.ceil(5.2))
print(math.ceil(5.8))
print(math.ceil(3.4))
print(math.ceil(17.6))    # bu komut yukarı yuvarlar



print("************************************************************")

# floor 

print(math.floor(5.2))
print(math.floor(5.8))
print(math.floor(3.4))
print(math.floor(17.6))    # ceil komutunun tersi yani tabana yuvarlar



print("************************************************************")

# trigonometrik fonksiyonlar


